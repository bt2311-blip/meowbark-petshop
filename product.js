function renderProduct(product) {
  const card = document.getElementById("product-card");
  const notFound = document.getElementById("product-not-found");
  const reviewsSection = document.getElementById("reviews-section");
  const descEl = document.getElementById("product-description");

if (descEl) {
  descEl.textContent = product.description || "Опис відсутній.";
}
  if (!card || !notFound || !reviewsSection) return;
  if (!product) {
    card.style.display = "none";
    reviewsSection.style.display = "none";
    notFound.hidden = false;
    return;
  }
  notFound.hidden = true;
  card.style.display = "";
  reviewsSection.style.display = "";
  const imgEl = document.getElementById("product-image");
  const nameEl = document.getElementById("product-name");
  const priceEl = document.getElementById("product-price");
  const catTag = document.getElementById("product-category-tag");
  const animalTag = document.getElementById("product-animal-tag");

  if (imgEl) {
    imgEl.src = product.image || "";
    imgEl.alt = product.name;
  }
  if (nameEl) nameEl.textContent = product.name;
  if (priceEl) priceEl.textContent = formatPrice(product.price);
  if (catTag) catTag.textContent = getCategoryLabel(product.category);
  if (animalTag) animalTag.textContent = getAnimalLabel(product.animal);

  card.dataset.id = product.id;
  card.dataset.name = product.name;
  card.dataset.price = product.price;
  card.dataset.image = product.image || "";
}

