function getReviewsKey(productId) {
  return `productReviews_${productId}`;
}
function loadReviews(productId) {
  try {
    const raw = localStorage.getItem(getReviewsKey(productId));
    return raw ? JSON.parse(raw) : [];
  } catch (e) {
    console.error("Помилка читання відгуків", e);
    return [];
  }
}
function saveReviews(productId, reviews) {
  try {
    localStorage.setItem(getReviewsKey(productId), JSON.stringify(reviews));
  } catch (e) {
    console.error("Помилка збереження відгуків", e);
  }
}
